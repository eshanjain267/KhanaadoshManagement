        obj.setDays((int)jsdays.getValue());
        obj.setLeaves((int)jsleave.getValue());
        obj.setPf((int)jspf.getValue());
        obj.setProfession_tax((int)jspro.getValue());
        
        obj.setAdvance((int)jsadv.getValue());
        obj.setBonus((int)jsbonus.getValue());
        obj.setInsurance((int)jsinsu.getValue());
        obj.setSpecial_allowance((int)jsallow.getValue());
        
        obj.setIncentive((int)jsinc.getValue());
        obj.setHra((int)jshra.getValue());
        obj.setConveyance_fixed((int)jsconf.getValue());
        obj.setConveyance_variable((int)jsconv.getValue());
