          obj.setEmpname(txtname.getText());
          obj.setDepartment(txtdep.getText());
          obj.setDesignation(txtdesi.getText());
          
          java.sql.Date d = new java.sql.Date(txtjoin.getDate().getTime());
          obj.setDate_of_join(d.toString());
          
