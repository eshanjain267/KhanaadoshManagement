    public boolean validateInput() {
        userid = txtuserid.getText();
        char[] pwd = txtpassword.getPassword();
        password = String.valueOf(pwd);
        System.out.println(password);
        if (userid.isEmpty() || password.length() == 0) {
            return false;
        } else {
            return true;
        }
    }

