    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        if (validateInput()) {
            String userType = getUserType();
            if (userType == null) {
                JOptionPane.showMessageDialog(null, "Please Select the User Type", "ERROR!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            try {
                login user = new login();
                user.setUserId(userid);
                user.setPassword(password);
                user.setUserType(userType);
                String username = loginDao.validateUser(user);
                if (username != null) {
                    JOptionPane.showMessageDialog(null, "Login Accepted!", "Welcome " + username, JOptionPane.INFORMATION_MESSAGE);
                    UserProfile.setUsername(username);
                    UserProfile.setUsertype(userType);

                  AdminOptionFrame adminFrame = new AdminOptionFrame();
                        adminFrame.setVisible(true);
                    
                    this.dispose();

                } else {
                    JOptionPane.showMessageDialog(null, "Invalid UserId/Password", "Login Denied!", JOptionPane.ERROR_MESSAGE);
                }

            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "ERROR IN SQL PART" + ex, "ERROR in DB!", JOptionPane.ERROR_MESSAGE);

                ex.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please Fill the Userid/Password", "EMPTY userid/Password!", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnloginActionPerformed

